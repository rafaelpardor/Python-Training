

#Una TUPLA es una cariable que permite almacenar varios datos inmutables
mi_tupla = ('cadena de tupla', 5, 2.36, 'otra lista', 25)
print(mi_tupla)

#Y se pude acceder a cada uno de los datos
print(mi_tupla[1])
print(mi_tupla[4])

# También se puede acceder a una porción de la tupla, indicando (opcionalmente) desde el índice de inicio hasta el índice de fin
print (mi_tupla[1:4])
print (mi_tupla[3:])
print (mi_tupla[:2])