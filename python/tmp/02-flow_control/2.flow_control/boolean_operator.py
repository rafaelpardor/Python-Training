
# Boolean operators with functions

print('')
def bool_and():
    print(True and True)
    print(True and False)
    print(False and True)
    print(False and False)
# -------------------------------------

print('')
def bool_or():
    print(True or True)
    print(True or False)
    print(False or True)
    print(False or False)
# -------------------------------------

print('')
def bool_not():
    print(not True)
    print(not False)
# -------------------------------------

if __name__ == '__main__':
    print('')
    bool_and()

    print('')
    bool_or()

    print('')
    bool_not()
    